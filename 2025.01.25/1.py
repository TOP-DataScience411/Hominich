import re

# Загрузка строк из файла
with open("history_dates_ed.txt", "r", encoding="utf-8") as f:
    lines = [line.strip() for line in f if line.strip()]

# Шаблоны без учёта описания после даты
patterns = {
    "YYYY–YYYY гг.": r"^\d{4}–\d{4} гг\.",
    "YYYY г.": r"^\d{4} г\.",
    "D Month YYYY г.": r"^\d{1,2} [а-яё]+ \d{4} г\.",
    "D–D Month YYYY г.": r"^\d{1,2}–\d{1,2} [а-яё]+ \d{4} г\.",
    "D Month – D Month YYYY г.": r"^\d{1,2} [а-яё]+ – \d{1,2} [а-яё]+ \d{4} г\.",
    "Month YYYY г.": r"^[а-яё]+ \d{4} г\.",
    "Month–Month YYYY г.": r"^[а-яё]+–[а-яё]+ \d{4} г\.",
    "D Month YYYY г. – D Month YYYY г.": r"^\d{1,2} [а-яё]+ \d{4} г\. – \d{1,2} [а-яё]+ \d{4} г\."
}

# Проверка совпадений
for line in lines:
    matched = [name for name, regex in patterns.items() if re.match(regex, line, re.IGNORECASE)]
    print(f"{line}\n  → {len(matched)}: {matched if matched else 'нет совпадений'}\n")

